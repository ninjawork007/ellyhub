            <h2>Instructions for Payee</h2>
            <p>
                You have received this form because you have either (al accepted payment cards for payments, or (bl received payments through a third party network in the calendar year reported on this form Merchant acquirers and third party settlement organizations, as payment settlement entities (PSE). must report the proceeds of payment card and third party network transactions made to you on Form 1099-K under Internal Revenue Code section 6050W. The PSE may have contracted with an electronic payment facilitator (EPF) or other third party payer to make payments to you.
            </p>
            <p>
                If you have questions about the amounts reported on this form, contact the FILER whose information is shown in the upper left corner on the front of this form. If you do not recognize the FILER shown in the upper left corner of the form, contact the PSE whose name and phone number are shown in the lower left corner of the form above your account number.
            </p>
            <p>
